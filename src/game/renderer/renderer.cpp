// TODO(szulf): doesnt handle transparent meshes and possibly more
void sort_items(Pass& pass)
{
  array_sort(
    pass.items,
    +[](const Item& a, const Item& b) -> bool
    {
      if (a.material != b.material)
      {
        return a.material > b.material;
      }
      return a.mesh > b.mesh;
    }
  );
}

